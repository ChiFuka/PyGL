def word(n):

    if n[0] == 1:
        return 'один'
    elif n[0] == 2:
        return 'два'
    elif n[0] == 3:
        return 'три'
    elif n[0] == 4:
        return 'четыре'
    elif n[0] == 5:
        return 'пять'
    elif n[0] == 6:
        return 'шесть'
    elif n[0] == 7:
        return 'семь'
    elif n[0] == 8:
        return 'восемь'
    elif n[0] == 9:
        return 'девять'

